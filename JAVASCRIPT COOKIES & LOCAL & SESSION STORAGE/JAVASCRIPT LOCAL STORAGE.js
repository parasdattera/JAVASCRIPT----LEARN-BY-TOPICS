/*

local storage acts like a storage which is locally available at anytime

even after closing restarting web browser the local storage data will not be deleted.
also this data not sent to server

The localStorage object allows you to save key/value pairs in the browser.

The localStorage object stores data with no expiration date.

The data is not deleted when the browser is closed, and are available for future sessions.

syntax -- window.localStorage -- or just -- localStorage

sntax -- Save Data to Local Storage

localStorage.setItem(key, value);

syntax -- Read Data from Local Storage

let lastname = localStorage.getItem(key);

Remove Data from Local Storage

localStorage.removeItem(key);

Remove All (Clear Local Storage)

localStorage.clear();

Parameters --  key(name of the key) , value(value of the key)









*/