/*
IIFE -- immidiately invoked function expression

a function expression where the code inside the function gets executed immidiately after it's been defined

how to created iife function

by using group operator  --- ()

we can create anonymous function inside group operator and and can execute it by just calling it using -- ();  like we used to call normal function
*/

// sample

(function (){
    console.log("IIFE FUNCTION .")
})();